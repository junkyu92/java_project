package 반복문;

public class 반복문2 {

	public static void main(String[] args) {
		int i = 0;									//시작값1
		while (i < 10) {							//조건식2
			System.out.println("밥먹을 시간이에요");		//3
			i++;									//4증감식
		}
		
		System.out.println(i);						//5
		
													//1 => (234)x10 => 5
	}//main

}
